int	*ft_readnbr(char *str, int *tab)
{
	int	i;
	int y;

	i = 0;
	y = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 48) && (str[i] <= 57))
		{	
			tab[y] = str[i] - 48;
			y++;
		}
		i++;
	}
	return str;
}
